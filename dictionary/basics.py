#dictionaries are used to represent ordered data like lists and tuple
#They store values based on key.
#Example
dict = {1: 'Aditya', 2: 'Nikhil'}
print(dict)

print(dict[1])      #here 1 is called the key and 'Aditya' the value

'''
We can update the values and also add new key, value pairs.
Example:
'''
dict[3] = "Sruthi"
print(dict[3])
