#in dictionaries there is no order. It is more like a map whose places we access using keywords.
dict = {1: 'Aditya', 2: 'Nikhil', 3: 'Sirisha', 4: 'Sruthi', 5: 'Vasantha', 6: 'Ravi', 10: 'Sridhar'}
counter = 1
while counter <12:
    print(dict[counter])
    counter += 1
